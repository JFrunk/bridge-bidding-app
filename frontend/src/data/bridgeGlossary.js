/**
 * Helper function to get terms by category
 */
export const getTermsByCategory = (categoryId) => {
  return glossaryTerms.filter(term => term.category === categoryId);
};

/**
 * Helper function to get terms by difficulty
 */
export const getTermsByDifficulty = (difficultyLevel) => {
  return glossaryTerms.filter(term => term.difficulty === difficultyLevel);
};

/**
 * Helper function to search terms
 */
export const searchTerms = (query) => {
  const lowerQuery = query.toLowerCase();
  return glossaryTerms.filter(term =>
    term.term.toLowerCase().includes(lowerQuery) ||
    term.definition.toLowerCase().includes(lowerQuery) ||
    (term.example && term.example.toLowerCase().includes(lowerQuery))
  );
};

/**
 * Helper function to get a term by ID
 */
export const getTermById = (id) => {
  return glossaryTerms.find(term => term.id === id);
};

/**
 * Helper function to get related terms for a given term
 */
export const getRelatedTerms = (termId) => {
  const term = getTermById(termId);
  if (!term || !term.relatedTerms) return [];

  return term.relatedTerms
    .map(relatedName =>
      glossaryTerms.find(t =>
        t.term.toLowerCase() === relatedName.toLowerCase() ||
        t.id === relatedName
      )
    )
    .filter(Boolean);
};

/**
 * Get all terms organized by category
 */
export const getTermsGroupedByCategory = () => {
  const grouped = {};
  Object.keys(CATEGORIES).forEach(categoryId => {
    grouped[categoryId] = getTermsByCategory(categoryId);
  });
  return grouped;
};

/**
 * Get statistics about the glossary
 */
export const getGlossaryStats = () => {
  const byDifficulty = {
    beginner: getTermsByDifficulty('beginner').length,
    intermediate: getTermsByDifficulty('intermediate').length,
    advanced: getTermsByDifficulty('advanced').length,
  };

  const byCategory = {};
  Object.keys(CATEGORIES).forEach(categoryId => {
    byCategory[categoryId] = getTermsByCategory(categoryId).length;
  });

  return {
    total: glossaryTerms.length,
    byDifficulty,
    byCategory,
  };
};

